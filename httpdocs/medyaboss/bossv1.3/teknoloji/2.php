							<p style="text-align:left;">"HAVACILIĞIN VECİHİLERİ BURADA YETİŞECEK"</p>

Okulda verilecek eğitimin, bireyleri iş hayatına hazırlamak amacıyla tasarlandığını kaydeden Özer, "Mesleki eğitimde yeni hedefimiz havacılık ve uzay. Savunma sanayi alanında sektörün ihtiyaç duyduğu nitelikli insan kaynağının ve yeni Vecihilerin yetişeceği uzay ve havacılık teknolojisi alanında açılacak bu lisemiz, Türkiye'de bir ilk olma özelliği taşıyor. Bu oldukça önemlidir. Bundan böyle havacılığın Vecihileri Elmadağ'da yetişecek inşallah. Savunma sanayimizin geleceği artık bu okulda yetişecek gençlerimize emanet. Hayali havacılık ve uzay olan öğrencilerimizi ve velilerimizi okulumuzu ziyaret etmeleri için bekliyoruz" ifadelerini kullandı. Ankara'nın Elmadağ ilçesinde bulunan Türkiye'nin havacılık ve uzay alanındaki ilk meslek lisesi görüntülendi.

Okulda, ayın yüzeyi şeklinde tasarlanmış fanus alanı, gezegenlerden oluşan dekorasyonu, uzay temalı duvarları, uçak kaldırma simülasyonu yer alıyor. Sınıflardaki tahtaların üzerinde ise Ulu Önder Atatürk'ün 'İstikbal göklerdedir' sözü yazılı.

Ankara Havacılık ve Uzay Teknolojileri Mesleki ve Teknik Anadolu Lisesi'nin müdürü Samet Bilecan, burada eğitim alacak öğrencilerin, ASELSAN, ROKETSAN, TUSAŞ gibi yerlerde iş imkanı bulacağını belirterek, "SSB ile iş birliği doğrultusunda okul, ROKETSAN fabrikasına 5 kilometre mesafede. Usta öğreticilerimiz ROKETSAN fabrikasından gelerek eğitimimize destek olacak. Çocuklarımıza o alanda eğitim verecekler. Tamamen üniversiteye yönelik çalışmalar devam edecek. Öğrencilerimizin mühendislikte; yani alanlarında başarılı bir öğrenci olarak yetişmesini sağlayacak. Öğrencilerin ASELSAN'a, ROKETSAN'a mühendis olarak girmesini hedefliyoruz" ifadelerini kullandı.
<p style="text-align:left;">"YÜZDE 1'LİK ÖĞRENCİ GRUBUNU HEDEFLİYORUZ"</p>
Okulda Anadolu ve fen lisesi programı uygulanacağını ifade eden Bilecan, "Okulumuzda havacılık ve uzay teknolojisi alanı var. Bu alanın 3 dalı var; tasarım ve imalat dalı, havacılık ve uzay elektronik sistemleri dalı ve uzay aracı itki sistemleri dalı var. Bu dallarda çocuklarımız bir uzay aracının veya bir uçağın üretiminde bulunacak, tasarım ve imalatı yapabilecek. Bu hava araçlarının tamamen elektronik sistemlerini geliştirebilecek ve pervane sistemlerini, motor sistemlerini çalıştırabilir duruma gelecek. Üretimini yapabilecek seviyeye gelecek. Okulumuzda itki sistemleri ve pervane sistemleri atölyelerimiz, montaj entegrasyon ve kontrol atölyemiz, elektronik sistemler, aviyonik sistemler atölyemiz var. Ayrıca uygulamalı alanlarda tasarım ve 3D yapmaları için de imkan var. Çocuklarımız teknolojiyle iç içe olacak ve bu alanda kendi imkanlarıyla burada başarıyı elde edecek. Yüzde 1'lik öğrenci grubunu hedefliyoruz. Çocuklarımızın kendilerini gerçekleştirmesini istiyoruz. Okulumuzda başarılı öğrencilerle büyük hedefler gerçekleştirmek istiyoruz. Çocuklarımızın hayallerini gerçekleştirmek istiyoruz" diye konuştu.

							
