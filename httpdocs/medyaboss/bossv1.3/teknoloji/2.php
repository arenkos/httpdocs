						<div style="padding-top:10px;text-align:justify;width:1000px;height:900px;font-size:20px;font-family:Poppins;">
							Türkiye'nin ilk havacılık ve uzay lisesi Ankara’nın Elmadağ ilçesinde açıldı. 2023-2024 eğitim öğretim yılından itibaren öğrenci alımına başlayacak lisenin eğitim süresi 1 yıl İngilizce hazırlık sınıfı dahil  5 yıl olacak. Milli Eğitim Bakanlığı'nca (MEB), Savunma Sanayii Başkanlığı (SSB) ve Türk Havacılık ve Uzay Sanayii A.Ş. (TUSAŞ) iş birliğinde uzay ve havacılık teknolojisi eğitim öğretimine yönelik çalışmalar tamamlandı.

2019 yılında imzalanan protokoller çerçevesinde uzay ve havacılık teknolojisi alanında Türkiye'nin ilk meslek lisesi, Ankara'nın Elmadağ ilçesinde inşa edildi. 2023-2024 eğitim öğretim yılına hazır hale getirilen okul, öğrenci ve veliler için ziyarete açıldı.

Milli Eğitim Bakanı Mahmut Özer, MEB'in dinamik mesleki ve teknik eğitim sistemi aracılığıyla uzay ve havacılık alanında harekete geçildiğini belirterek, "SSB ile TUSAŞ iş birliğinde ilk defa havacılık ve uzay teknolojileri alanında eğitim verecek lisenin açılması için çalışmalarımızı tamamladık. Yapılan çalışma kapsamda havacılık ve uzay teknolojisi eğitiminin yaygınlaştırılması, eğitim içeriklerinin geliştirilmesi ve zenginleştirilmesi amacıyla hazırlanan havacılık ve uzay teknolojisi alanı öğretim programı Talim ve Terbiye Kurulu'nda kabul edildi." dedi. 
<p style="text-align:left;">"OKULUMUZ TÜM İMKANLARA SAHİP"</p>
Müfredatın; SSB, Türk Hava Kurumu Üniversitesi, Askeri Elektronik Sanayi A.Ş. (ASELSAN), Roket Sanayii ve Ticaret A.Ş. (ROKETSAN) ve TUSAŞ temsilcileri ile alan öğretmenlerinden oluşan komisyon tarafından hazırlandığına vurgu yapan Özer, "Hazırlanan çerçeve öğretim programı kapsamında eğitim verecek olan ilk meslek lisemizi, Ankara'nın Elmadağ ilçesinde açtık. 24 bin 770 metrekarelik alanda kurulan okulumuz; eğitim binası, atölye binası ve pansiyon binası olmak üzere 3 ana binadan oluşuyor. Okulda 32 derslik, 6 laboratuvar, 2 resim ve müzik atölyesi ile konferans salonu bulunuyor. Okul pansiyonu 64 kız öğrenci ve 136 erkek öğrencinin konaklamasına uygun olmak üzere 200 yatak kapasitene sahiptir. İl dışından alınacak öğrenciler banyo ve lavaboları oda içerisinde olan, bilgisayar ve internet bağlantısı bulunan tek kişilik odalarda barınacak. Okulumuz gerekli tüm atölye ve laboratuvar imkanına sahip. Ankara Havacılık ve Uzay Teknolojileri Mesleki ve Teknik Anadolu Lisesi, proje okulu kapsamında eğitim verecek. Okulun yönetici ve öğretmenleri seçilerek alınacak. Okul, 2023-2024 eğitim öğretim yılından itibaren öğrenci alımına başlayacak. 1 yıl İngilizce hazırlık sınıfı dahil 5 yıl süreyle eğitim öğretim verilecek ve tüm öğrenciler Bakanlığımızca yapılan merkezi sınav puanına göre alınacak." ifadelerini kullandı. 
