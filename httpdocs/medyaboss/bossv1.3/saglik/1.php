Sağdur, günlük 10 bin ünite bandında kan bağışı devam ettiği sürece ne olağanüstü ne acil ne de rutin durumlarda sıkıntı yaşanmayacağını belirterek, "Ramazanda kan bağışlarıyla ilgili yaşanan dönemsel sıkıntı net bir şekilde aşıldı, kan bağışlarımız bir haftadır günlük 9-10 bin bandında devam ediyor. Vatandaşlarımıza teşekkür ediyoruz." açıklamasında bulundu. Sıcak havalarda da kan bağışının azalabildiğine işaret eden Sağdur, kanın acil değil sürekli bir ihtiyaç olduğuna dikkati çekti.

Türk Kızılayın "kanver.org" isimli sitesinden kan bağış noktalarının haftalık paylaşıldığını, kişilerin bu noktaları kontrol edip istediğinde kan bağışı randevusu da alabileceğini anlatan Sağdur, "Kan ihtiyacı ancak toplumsal dayanışma ve yardımlaşmayla çözülebilir. Kanın yapay olarak üretilmesi veya yurt dışından getirilmesi mümkün değil. Hastanede ihtiyacı olan kişiler için illa bir başka vatandaşın bağışta bulunması gerekiyor." çağrısında bulundu. Sağdur, Türk Kızılayın "kan sattığı" iddialarının asla gerçeği yansıtmadığını vurgulayarak, "Bu yönde kurulmuş bir sistem yok. Vatandaşlarımızın hastanelerdeki ihtiyacını tamamen devletimiz karşılıyor ve bunun üzerine kurulmuş bir sistem uygulanıyor." dedi.

Dünyada kan bankacılığı uygulamalarında en gelişmiş sistemi uyguladıklarını aktaran Dr. Levent Sağdur, bu sistemin dünyanın birçok ülkesinde de Türk Kızılay vasıtasıyla hayata geçirildiğini, Kızılay uzmanlarının bu ülkelere danışmanlık verdiğini kaydetti.

